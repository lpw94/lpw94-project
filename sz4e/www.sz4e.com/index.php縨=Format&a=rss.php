一、空间导向的特性<br />
1、与建筑同筑<br />
在建筑空间环境中，导向系统除了需要文字、图像、图形、符号等信息以及载体来构建，灯光、色彩、材料及路径等也都在这个系统中扮演着重要的角色。与其相关的地面、墙面图形、雕塑、载体形式等也都成为潜在的识别要素，具有视觉识别、导向和艺术象征的功能。这些潜在的要素是城市导识系统艺术化创造的物质基础。<br />
2、与环境共融<br />
对于个人来讲，每个人都有自身的文化认同和审美取向，但在公共环境中， 城市导识对于公众视觉生理和心理上的共通性或舒适性却更为重要， 这使公共艺术形态下的城市导示系统设计不能以个人的好恶为出发点来进行随心所欲的创造， 必须考虑到公众的文化认同感以及对特定的地域环境特征。尊重自然地貌、气候、光照等原始环境特征，关照人流及时间产生全方位因素，使人文景观与自然景观相依共存，尊重环境并非简单的使用环境， 是公共艺术形态下城市导识设计的价值取向。<br />
二、空间导向中艺术形态的构成要素<br />
1、图形符号<br />
图形符号有比文字更好的视认性， 因为图形是对现实物象的模拟，一见到图形就能联想到现实物象，而文字是抽象符号要经过人脑信息处理转化人才能对其所要表达的概念有映，而且图形的辨识不受国家、民族语音和文化程度的局限。<br />
2、色彩文字<br />
色彩能够有效加强标识的识别效果， 有时甚至能够代替文字表达特定的涵义， 尤其色彩的象征意义甚至比文字更容易理解，更能符合国际化、全球化的标准。文字是视觉导向设计中最重要的元素，文字能够传递复杂的信息，图形则便于快速识认，但是因为图形解读的多义性，不确定性，导向信息是不可能只由图形来传达，尤其是复杂的环境信息，它们通常是互相配合作用。<br />
三、空间导向中艺术形态的多元化<br />
1、抽象的艺术形态<br />
构成抽象形态的基本形式有点、线、面、体等。但是，设计作品的形态美仅在形状来定义是远远不够的。设计物的形态创造要善于运用变化与统一、韵律与节奏等多种造型手法，以达到传达视觉信息的目的。在造型的形态因素中，它包含了形状、肌理、还有色彩。这三方面在形态表现中缺一不可。色彩作为其表现因素， 在笔者看来是一种既抽象又富于情感变化的形态组成。艺术的目的是通过感情的传递引起感情的共鸣和心灵的净化。所以必须使色彩在设计中表达一定的情感和意义才能使设计作品更有人情味。<br />
2、可视符号化的艺术形态<br />
可视符号化体现在对于公共空间的可视过程， 使用者通过视觉信息明确场所、方位、道路和行为的作用。可视符号化具有意象性，是城市意象的营造，是以城市居民在心理上对于所处环境进行清晰识别为出发点， 能将城市文脉通过物化的方式转化为城市意象，实现精神空间上的导识。可视符号化的艺术形态造型上符合动感与朝向性， 运用图形来说明标识的功能来导向人流。外来人员可以通过视觉，切实感受到本地域的独特文脉。<br />
3、雕塑的艺术形态<br />
雕塑本身是一种古老的艺术形态， 是人们在三维空间中创造出的新的审美实体，表达自己对周围环境的认识。将雕塑艺术与导向标识的功能相结合， 既有实用功能又具有艺术审美价值， 使人们在使用导向设施获取信息的同时又在享受着艺术内涵。雕塑导向设计属于艺术性导向的一类，它具有形象标志性特征，不仅能起到导向、识别的作用，还具有艺术性和文化性的特质。在设计雕塑性导向标识时，需要考虑与周围环境的协调，充分融入空间环境与人文环境之中，使雕塑导向的艺术性与城市地域个性化以及人们的审美价值相统一。<br />
空间导向的艺术形态多元化发展趋向于城市导向设计美学风格和艺术特征的多样化。多元化既体现在不同的公共环境中的各式各样的导向设计表现风格上， 又表现在同一导向设计中多种艺术表现风格的融合上，是传承城市、街区的历史与文化脉络，同时找到并形成一个有效的艺术表现手段。<br />]]></description><pubDate>Thu, 02 Apr 2015 14:40:40 GMT</pubDate><author>linkpai</author></item><item><title>品牌标识设计必看设计常识</title><link>http://www.sz4e.com/news/show_175.html</link><description><![CDATA[<p class="MsoNormal">
